void UtilityHexToAscii(T_U16 hexadecimal, T_U8 digits, T_U8 *ascii){
    T_U8 i;
    T_U16 divisor;
    T_U8  temp;
    for(i=0;i<digits;i++){
        divisor = power(16, digits - i - 1);
        temp=hexadecimal / divisor;
        if(temp<=9){
            ascii[i] = hexadecimal / divisor + 0x30;
        }
        else if(temp>=0x0A&&temp<=0x0F){
            ascii[i] = hexadecimal / divisor + 0x37;
        }
        hexadecimal %= divisor;
    }
}
