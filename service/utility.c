T_U8 UtilityGenU8(T_U8 from_bit,T_U8 to_bit){
    T_U8 i;
    T_U8 temp_u8 = 0;
    
    M_Assert(from_bit < 8);
    M_Assert(to_bit < 8);
    M_Assert(to_bit >= from_bit);

    for (i=from_bit;i<=to_bit;i++){
        temp_u8 += (T_U8)(1<<i);
    }

    return temp_u8;
}

T_U8 UtilityReverseU8(T_U8 be_reversed_u8){
    T_U8 need_reversed_u8 = 0;
    T_U8 reversed_u8 = 0;
    T_U8 i = 0;

    need_reversed_u8 = be_reversed_u8;
    for (i=0;i<8;i++){
        if ((need_reversed_u8 << i) & 0x80){
            reversed_u8 |= (1 << i);
        }
    }

    return reversed_u8;
}

static T_U16 power(T_U8 base, T_U8 exp){
    T_U16 power;
    T_U8 i;

    power = 1;
    for (i = 0; i < exp; i++){
        power *= base;
    }
    return power;
}
// ASCII字符串转为十进制数(1个字节)
 void UtilityAsciiToDec(T_U8 *ascii, T_U8 digits, T_U8* decimal){
    T_U8 digit;
    T_U8 i;

    *decimal = 0;
    for (i = 0; i < digits; i++){
        if (ascii[i] < '0' || ascii[i] > '9'){
            *decimal = 0;
            break;
        }
        digit = ascii[i] - '0';
        *decimal += digit * power(10, digits - i - 1);
    }
}
// ASCII字符串转为十六进制数(1个字节)
 void UtilityAsciiToHex(T_U8 *ascii, T_U8 digits, T_U8* hexdecimal){
    T_U8 digit;
    T_U8 i;

    *hexdecimal = 0;
    for (i = 0; i < digits; i++){
        if (ascii[i] < '0' || ascii[i] > 'F'){
            *hexdecimal = 0;
            break;
        }
        if(ascii[i] >= '0' && ascii[i] <= '9'){
            digit = ascii[i] - '0';
        }
        else if(ascii[i] >= 'A' && ascii[i] <= 'F'){
            digit = ascii[i] - '7';
        }
        *hexdecimal += digit * power(16, digits - i - 1);
    }
}
// 十进制数转为ASCII字符串
 void UtilityDecToAscii(T_U16 decimal, T_U8 digits, T_U8 *ascii){
    T_U8 i;
    T_U16 divisor;

    for (i = 0; i < digits; i++){
        divisor = power(10, digits - i - 1);
        ascii[i] = decimal / divisor + '0';
        decimal %= divisor;
    }
}
