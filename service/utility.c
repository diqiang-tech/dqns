void UtilityIntToAsciiStr(T_U32 local_int,T_U8* p_str){
    T_U8 p_temp_str[MAX_NUMBER_LENGTH+1];
    T_U32 temp_answer = 0;
    T_U8 temp_remainder = 0;
    T_U8 index = 0;
    T_U8 i = 0;

    M_Assert(p_str);
    
    if (0 == local_int){
        p_str[0] = '0';
        p_str[1] = 0;
        return;
    }

    temp_answer = local_int/10;
    temp_remainder = local_int%10;
    
    if (0 == temp_answer){
        p_str[0] = '0'+temp_remainder;
        p_str[1] = 0;
        return;
    }

    while(temp_answer){
        p_temp_str[index] = '0'+temp_remainder;

        temp_remainder = temp_answer%10;
        temp_answer = temp_answer/10;

        index++;
        
    }
    p_temp_str[index] = '0' + temp_remainder;
    index++;
    p_temp_str[index] = 0;

    for (i=0;i<index;i++){
        p_str[i] = p_temp_str[index-1-i];
    }

    p_str[index] = 0;
    
}


void UtilityFloatToAsciiStr(T_U32 float_number,T_U8 decimal_width,T_U8* p_str){
    T_U8 p_temp_str[MAX_NUMBER_LENGTH+1];
    T_U8 str_len;
    T_U8 i;
 
    UtilityIntToAsciiStr(float_number,p_temp_str);

    if (0 == decimal_width){
        strcpy((char*)p_str,(const char*)p_temp_str);
        return;
    }

    str_len = strlen((char const*)p_temp_str);
    
    //float_number=12,  decimal_width=1,  str='1.2'
    if (str_len > decimal_width){

        for (i=0;i<decimal_width;i++){
            p_temp_str[str_len-i] = p_temp_str[str_len-i-1];
        }

        p_temp_str[str_len-decimal_width] = '.';
        p_temp_str[str_len+1] = 0;


        for (i=0;i<=(str_len+1);i++){
            p_str[i] = p_temp_str[i];
        }

    }
    //float_number=12,  decimal_width=2,  str='0.12'
    else if (str_len == decimal_width){
        for (i=0;i<decimal_width;i++){
            p_temp_str[str_len+1-i] = p_temp_str[str_len-1-i];
        }
        p_temp_str[0] = '0';
        p_temp_str[1] = '.';
        p_temp_str[str_len+2] = 0;

        strcpy((char*)p_str,(const char*)p_temp_str);
    }
    
}


