T_U16 UtilityGenCrc16(T_U8* p_data,T_U8 data_len){
    T_U8 index;
    T_U8 crch;
    T_U8 crcl;
    T_U16 crc16 = 0;
    T_U8 i;

    M_Assert(p_data);
    M_Assert(data_len>0);

    crch=0xFF;
    crcl=0xFF;
    
    for(i=0;i<data_len;i++){
        index=crch^p_data[i]; 
        crch=crcl^cg_CRCTABH[index];
        crcl=cg_CRCTABL[index];    
    }

    crc16 = crch;
    crc16 = crc16<<8;
    crc16 = crc16&0xFF00;
    crc16 = crc16|crcl;

    return crc16;
}


T_U16 UtilityGetAverage(T_U16* p_data,T_U8 data_count){
    T_U8 i;
    T_U16 data_sum;
    T_U16 data_min;
    T_U16 data_max;

    data_sum = 0;
    data_min = 0;
    data_max = 0;

    M_Assert(p_data);
    M_Assert(data_count);
    
    for (i=0;i<data_count;i++){
        data_sum += p_data[i];
    }
    
    if (data_count <= 2){
        return (data_sum/data_count);
    }

    data_min = UtilityGetMin(p_data,data_count);
    data_max = UtilityGetMax(p_data,data_count);

    data_sum -= (data_min + data_max);

    return (data_sum/(data_count - 2));
    
}

T_U16 UtilityGetMax(T_U16* p_data,T_U8 data_count){
    T_U8 i;
    T_U16 data_max;
    
    M_Assert(p_data);
    M_Assert(data_count);

    data_max = 0;

    for (i=0;i<data_count;i++){
        if (p_data[i] > data_max){
            data_max = p_data[i];
        }
    }

    return data_max;
}

T_U16 UtilityGetMin(T_U16* p_data,T_U8 data_count){
    T_U8 i;
    T_U16 data_min;
    
    M_Assert(p_data);
    M_Assert(data_count);

    data_min = MAX_T_U16;

    for (i=0;i<data_count;i++){
        if (p_data[i] < data_min){
            data_min = p_data[i];
        }
    }

    return data_min;
}


